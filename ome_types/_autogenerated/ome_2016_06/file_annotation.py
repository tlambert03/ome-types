    """A file object annotation."""

    class Meta:
        namespace = "http://www.openmicroscopy.org/Schemas/OME/2016-06"

    binary_file: BinaryFile = Field(
        json_schema_extra={
            "name": "BinaryFile",
            "type": "Element",
            "required": True,
        }
    )
