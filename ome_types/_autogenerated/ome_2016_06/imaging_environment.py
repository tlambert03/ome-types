from typing import Optional

from pydantic import Field

from ome_types._autogenerated.ome_2016_06.map import Map
from ome_types._autogenerated.ome_2016_06.units_pressure import UnitsPressure
from ome_types._autogenerated.ome_2016_06.units_temperature import (
    UnitsTemperature,
)
from ome_types._mixins._base_type import OMEType

__NAMESPACE__ = "http://www.openmicroscopy.org/Schemas/OME/2016-06"


class ImagingEnvironment(OMEType):
    """
    This describes the environment that the biological sample was in during the
    experiment.

    Attributes
    ----------
    map : None | Map
        (The ImagingEnvironment Map).
    temperature : None | float
        The Temperature is the define units.
    temperature_unit : UnitsTemperature
        The units the Temperature is in - default:Celsius[Â°C].
    air_pressure : None | float
        AirPressure is the define units.
    air_pressure_unit : UnitsPressure
        The units the AirPressure is in - default:millibars[mbar].
    humidity : None | float
        Humidity around the sample [units:none] A fraction, as a value from 0.0 to
        1.0.
    co2_percent : None | float
        Carbon Dioxide concentration around the sample [units:none] A fraction, as
        a value from 0.0 to 1.0.
    """

    class Meta:
        namespace = "http://www.openmicroscopy.org/Schemas/OME/2016-06"

    map: Optional[Map] = Field(
        default=None,
        json_schema_extra={
            "name": "Map",
            "type": "Element",
        },
    )
    temperature: Optional[float] = Field(
        default=None,
        json_schema_extra={
            "name": "Temperature",
            "type": "Attribute",
        },
    )
    temperature_unit: UnitsTemperature = Field(
        default=UnitsTemperature.CELSIUS,
        json_schema_extra={
            "name": "TemperatureUnit",
            "type": "Attribute",
        },
    )
    air_pressure: Optional[float] = Field(
        default=None,
        json_schema_extra={
            "name": "AirPressure",
            "type": "Attribute",
        },
    )
    air_pressure_unit: UnitsPressure = Field(
        default=UnitsPressure.MILLIBAR,
        json_schema_extra={
            "name": "AirPressureUnit",
            "type": "Attribute",
        },
    )
    humidity: Optional[float] = Field(
        default=None,
        ge=0.0,
        le=1.0,
        json_schema_extra={
            "name": "Humidity",
            "type": "Attribute",
            "min_inclusive": 0.0,
            "max_inclusive": 1.0,
        },
    )
    co2_percent: Optional[float] = Field(
        default=None,
        ge=0.0,
        le=1.0,
        json_schema_extra={
            "name": "CO2Percent",
            "type": "Attribute",
            "min_inclusive": 0.0,
            "max_inclusive": 1.0,
        },
    )
